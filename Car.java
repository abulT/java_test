public class Car{
    private String engine;
    private int doors;
    private int tyres;

    public String getengine(){
        return engine;
    }
    public void setengine(String engine){
        this.engine = engine;
    }
    public int getdoors(){
        return doors;
    }
    public void setdoors(int  doors){
        this.doors = doors;
    }
    public int gettyres(){
        return tyres;
    }
    public void settyres(int tyres){
        this.tyres = tyres;
    }

}